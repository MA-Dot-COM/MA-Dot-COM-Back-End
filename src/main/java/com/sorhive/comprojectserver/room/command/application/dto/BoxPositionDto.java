public class BoxPositionDto {
    private Double x;
    private Double y;
    private Double z;

    public BoxPositionDto() {
    }

    public BoxPositionDto(Double x, Double y, Double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public Double getX() {
        return x;
    }

    public Double getY() {
        return y;
    }

    public Double getZ() {
        return z;
    }
}
